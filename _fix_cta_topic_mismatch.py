fixed = []
for fname, old_anchor, new_anchor in fixes:
    p = pathlib.Path(fname)
    text = p.read_text(encoding='utf-8')
    cta_pos = text.find('id="service-cta"')
    if cta_pos == -1:
        print(f'NO CTA BLOCK: {fname}')
        continue
    anchor_pos = text.find(old_anchor, cta_pos)
    if anchor_pos == -1:
        print(f'ANCHOR NOT FOUND AFTER CTA: {fname}  (total occurrences: {text.count(old_anchor)})')
        continue
    new_text = text[:anchor_pos] + new_anchor + text[anchor_pos + len(old_anchor):]
    p.write_text(new_text, encoding='utf-8')
    print(f'FIXED: {fname}  ->  {new_anchor}')
    fixed.append(fname)

print(f'\nFixed {len(fixed)}/{len(fixes)} files')
